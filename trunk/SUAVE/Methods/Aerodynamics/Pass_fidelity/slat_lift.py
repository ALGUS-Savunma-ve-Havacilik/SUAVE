#-------------slat correction-----------------------------
def slat_lift(sa,sw):  # formatting

    #sa=10   #slat angle in degrees
    
    #sw=30  #sweep degrees
    #cl inc without sweep
    sl_cl_inc = -7E-07*sa**5 + 6E-05*sa**4 - 0.0016*sa**3 + 0.0177*sa**2 - 0.0295*sa + 0.021
    
    dcl_sweep_dcl = -0.0002*sw**2 - 0.0035*sw + 0.9966
    dcl_slat=sl_cl_inc*dcl_sweep_dcl
    return dcl_slat

